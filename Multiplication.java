import java.util.*;
public class Multiplication{
    public static void main (String args[]){
        Scanner mul=new Scanner(System.in);
        System.out.print("Enter the value :  ");
        int m=mul.nextInt();
        int i;
        int j;
        int p;
        for(i=m; i==m;i++){
            for(j=1;j<=20;j++){
                p=m*j;
                System.out.println(m+"*"+j+"="+p);
            }
        }
    }
}